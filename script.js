function getSelected() {
  const answerEls = document.querySelectorAll("#answer");
  let answer = undefined;
  answerEls.forEach((answerEl) => {
    if (answerEl.checked) {
      answer = answerEl.id;
    }
  });
  return answer;
}

function unSelected() {
  const answerEls = document.querySelectorAll("#answer");
  answerEls.forEach((answerEl) => {
    if (answerEl.checked) {
      answerEl.checked = false;
    }
  });
}

function scoring() {
  const answerEls = document.querySelectorAll("#answer");
  if (answerEls[0].checked) {
    score += quizData[currentQuiz].poinA;
  } else if (answerEls[1].checked) {
    score += quizData[currentQuiz].poinB;
  } else if (answerEls[2].checked) {
    score += quizData[currentQuiz].poinC;
  } else if (answerEls[3].checked) {
    score += quizData[currentQuiz].poinD;
  } else if (answerEls[4].checked) {
    score += quizData[currentQuiz].poinE;
  } else if (answerEls[5].checked) {
    score += quizData[currentQuiz].poinF;
  } else {
    return;
  }
  total = score / 29;
}

